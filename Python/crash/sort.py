places = ['london', 'germany', 'canada', 'san jose', 'new york', 'paris']
print(places)

print('Here is the original list: ')
print(places)

print("here is the sorted list: ")
print(sorted(places))

print("here is the original list order again: ")
print(places)

print("Here is the reverse order of sorted: ")
print(sorted(places, reverse=True))

print(places)

places.reverse()
print(places)

places.reverse()
print(places)

places.sort()
print(places)

places.sort(reverse=True)
print(places)
